<h4><span class="label">Search Questions</span></h4>
  <dd class="accordion-navigation">
    <a href="#panelS1">1. I am getting 0 results after I request a search.</a>
    <div id="panelS1" class="content">
	<ul>
		<li>Check your calendar dates - sometimes the shows that you're expecting to see are outside of your selected date range.</li>
		<li>Your search for fixed programming cannot go past the default end date.  Check the Projected Calendar for any shows that may be available outside of the 8 week ShowSeeker window.</li>
		<li>If you are searching for an end time of midnight, make sure you use 11:59pm rather than 12A.  Example: If you want to search from 8pm to midnight, set your time range to 8pm to 11:59pm.  This is the most common cause of 0 results.</li>
		<li>Did you hit your red "reset" button after your last search?  Many times a User will have chosen something (example: you previously searched for new programming clicking the "Only New" category). That choice will remain selected on your next search if you don't "uncheck" it or hit the reset button.  If you now try to search for programming which doesn't have any New shows, you will get 0 results.  Always check the red "reset" or orange "filters" button before doing your next search.</li>
	</ul>
    </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS2">2. Why doesn't ShowSeeker display programming information further than 8 weeks out?</a>
    <div id="panelS2" class="content">
      ShowSeeker receives programming data from Gracenote, formerly Tribune Media Services (TMS), the industry leader in television programming information.  Gracenote receives their information directly from the networks and has a large staff that constantly updates this data.  The 8-week window displays the programming that the networks are willing to release.  ShowSeeker updates this data every 24 hours so you are always getting the most current data available with every search.  There are some networks that will not release programming data beyond 2 or 3 weeks and unfortunately we don't have any control over that.  However, we now have the Projected Calendar which lists hundreds of episodic shows, sports, and other programming beyond the ShowSeeker 8-week window.
    </div>
  </dd>


  <dd class="accordion-navigation">
    <a href="#panelS3">3. What's the difference between the DMA and Zone choices in Basic Settings?</a>
    <div id="panelS3" class="content">
      In the Zone drop-down area, your Zones are listed first, followed by any DMAs that are in your Market.  A Zone contains insertable Cable networks, whereas the DMA selection contains programming for Broadcast stations in your Market.  Most of your searches and proposals will be created in one of your Cable Zones.  However, the DMA selection allows you to find Broadcast airings of Olympics, NFL football, PGA events, and any other programming that may be of interest to you.
    </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS4">4. How do I save my search results?</a>
    <div id="panelS4" class="content">
      When you have all your desired parameters in (such as keywords, genre, time and date range, show type, etc.), just click the blue "Save" button at the bottom of your screen.  A new "Save Search" window will open which will allow you to name your search and make any notes about this saved search.  Then click "Save".  Your saved criteria will be listed under the heart icon on the top menu bar.  The next time you want to do that search, just go to the heart icon and click on the appropriate line. It will automatically fill in all those parameters, with the current dates or titles.
    </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS5">5. The network grids, website or affidavit says a different show than I ordered.</a>
    <div id="panelS5" class="content">
     Most cases of conflicting programming data are due to quarterly grids or network websites that aren't updated every 24 hours as ShowSeeker data is.  It can also be caused by a traffic system such as NOVAR that uses their own data for Affidavits and does not reflect the same updated data as ShowSeeker uses.  A network can also move a program to a different date or time and we are not alerted to that fact.  It is best for you to check  your schedule against your LIVE Grids platform a few days before the schedule runs to ensure the programs you have scheduled have not been moved.  If you have questions about a discrepancy, please email us at <a href="mailto:support@showseeker.com">support@showseeker.com</a> and we will investigate for you.
    </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS6">6. I am in Title Search and my results are not displaying all the shows that I expected to see.  What do I do?</a>
    <div id="panelS6" class="content">
      Sometimes your cache needs to be refreshed. Just press the F5 button on your keyboard.  That will refresh your screen and the programming will reload. The other option is to exit out of your account, close the browser windows, and open ShowSeeker again.  It may also be that the Title you are searching for is not yet in the ShowSeeker database, in which case you may be able to find it in the Projected Calendar.
    </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS7">7. I am using the "Genre" category, but I don't see the genre I am looking for.</a>
    <div id="panelS7" class="content">
       Because the "Genre" function is dynamic, it will only show genres for the criteria you have chosen (such as date range, time range, network, etc.). To see ALL available genres, press the red "reset" button, and then perform the search. All the genres available over the next 56 days will load and you can see if the genre you are looking for is available.
     </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS8">8. My results are grouped together and I need to see each show within a group. </a>
    <div id="panelS8" class="content">
       You have changed your Results to Grouped mode.  Click on "Grouped" to change back to "Fixed" mode to view the individual results. If you would like to view the results for a particular line, you can open up just that line by clicking on the + sign.
     </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS9">9. No networks are loading.</a>
    <div id="panelS9" class="content">
       Make sure a Zone is selected.
     </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelS10">10. A holiday is coming up and I'm trying to find pertinent shows to the holiday.  What is the best way to find them?</a>
    <div id="panelS10" class="content">
       Using a combination of the Genre category and Keyword search is ideal, especially for Holidays.  For Halloween the "Paranormal" or "Horror" genres will generally get hundreds of results, also Keywords such as Halloween, ghost, vampire, etc.  For Thanksgiving or Christmas, genres such as "Holiday" or "Holiday Specials" will yield the best results, along with Keywords such as Santa, turkey, Thanksgiving and/or Christmas.
     </div>
  </dd>	

  <dd class="accordion-navigation">
    <a href="#panelS11">11.  I can't find new shows (as an example, Duck Dynasty) in ShowSeeker beyond 4 of them.  How do I add more shows outside of what is showing in ShowSeeker?</a>
    <div id="panelS11" class="content">
       Create a new schedule and add the ShowSeeker programs.  Then go to the Projected Calendar and look for the additional shows on future dates.  You may then check the boxes to add to your schedule.  The shows from the Projected Calendar will show in your Proposal as "projected" as a reminder to run the search before they air to make sure these shows have not been moved by the network.
     </div>
  </dd>	

  <dd class="accordion-navigation">
    <a href="#panelS12">12.  When I enter a name into Title Search, I get back results for other shows, sometimes 5000 results.  What is happening?</a>
    <div id="panelS12" class="content">
       When you start entering a name into Title Search, you will see the results filtering below the Title Search box.  Once you see the title or titles you want, click on them and make sure they turn green, then drag them over to the right side of the dialog box with your mouse.  Once you have all the titles you want to search for (you can also click on "New" if you only want new programs), click on "Search ShowSeeker".
     </div>
  </dd>	

<h4><span class="label">Navigation Questions</span></h4>

   <dd class="accordion-navigation">
    <a href="#panelN1">1. ShowSeeker is "frozen", the loading icon keeps spinning and won't load or some of the Menu Bar icons are blank.</a>
    <div id="panelN1" class="content">
		The most common cause is caused by Internet Explorer being set in compatibility mode.  Please follow the steps in this <a href="http://showseeker.com/tutorials/guides/ShowSeeker_Compatibility-View_and_Browser-Issues.pdf" target="_blank">PDF file </a> to remedy the situation.   If compatibility mode does not appear to be the problem, press the F5 key on your keyboard to refresh your connection or you can log out of your account and log back in again.  If the problem is still not resolved, please email us at <a href="mailto:support@showseeker.com">support@showseeker.com</a> for assistance.
    </div>
  </dd>

    <dd class="accordion-navigation">
    <a href="#panelN2">2. When do I use the "Shift" key and the "Control" key?</a>
    <div id="panelN2" class="content">
      <ul>
		<li>Use the Shift key on your keyboard along with the left click on your mouse to select a range of shows.</li>
		<li>To select individual shows, keep the CTRL key depressed, using the "left" click on the mouse to select your shows. Once highlighted you can drag any number of shows down to a proposal.</li>
	</ul>
    </div>
  </dd>

    <dd class="accordion-navigation">
    <a href="#panelN3">3. How do I delete a proposal?</a>
    <div id="panelN3" class="content">
      To delete a proposal, click on the unwanted titles' checkbox.  This will highlight the Proposal, next click the red trash can near the top of the proposal window, a warning dialog will appear asking if you are sure - click "ok" and the proposal will be deleted. 
    </div>
  </dd>

    <dd class="accordion-navigation">
    <a href="#panelN4">4. How do I delete programs I accidentally dragged into my proposal?</a>
    <div id="panelN4" class="content">
      Simply highlight the show(s) you wish to remove (using your Shift or CTRL key) and click the "delete" key on your keyboard or click on the red trash can located on the proposal title bar. 
    </div>
  </dd>

    <dd class="accordion-navigation">
    <a href="#panelN5">5. My browser is detecting zoom.</a>
    <div id="panelN5" class="content">
      ShowSeeker Plus is best viewed at 100% zoom level.  If you feel that your browser may not be at 100% zoom, simply use the CTRL + or CTRL - to change the zoom factor.  Zoom options can also be found in the settings for each browser. 
    </div>
  </dd>

<h4><span class="label">Downloads & Export Questions</span></h4>

  <dd class="accordion-navigation">
    <a href="#panelE1">1. I exported my proposal into Excel but now I can't find it.</a>
    <div id="panelE1" class="content">
      Always make sure when you export to any format, you click "save as" and not "open" on the dialog box. You will want to save all your proposals into your own folders or on your desktop. If you choose the "open" option, that saves your document to a temporary internet file location, and it is likely you won't be able to find it.
    </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelE2">2. Should I be using the email option on the Download tab to email my proposals to my clients?</a>
    <div id="panelE2" class="content">
      We generally suggest that you save your proposals to your clients' files then email from your own servers.  However, we do have an email function available on the Download tab if you need to email from your ShowSeeker account.
    </div>
  </dd>

  <dd class="accordion-navigation">
    <a href="#panelE3">3. I don't want the logos and/or network descriptions on my proposal.</a>
    <div id="panelE3" class="content">
      On the "Download" tab, you will see a box that says "Include Logos" and "Include Description".  ShowSeeker defaults to both options. If you don't want those on your document, simply "uncheck" either or both options.
    </div>
  </dd>


  <dd class="accordion-navigation">
    <a href="#panelE4">4. I am trying to download a schedule but it will not download to any of the formats.  What could be wrong?</a>
    <div id="panelE4" class="content">
      Make sure you have dragged the shows from the top Results window down to the bottom Proposal window so that your proposal has spots listed in it.  If you are still having download/export issues, please contact Support at <a href="mailto:support@ShowSeeker.com">support@ShowSeeker.com</a> 
    </div>
  </dd>

